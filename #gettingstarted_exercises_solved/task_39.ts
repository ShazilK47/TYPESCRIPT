function city_country(city: string, country: string) {
  return `${city}, ${country}`;
}

let pair_1 = city_country("Paris", "France");
let pair_2 = city_country("karachi", "Pakistan");
let pair_3 = city_country("Makkah", "Saudi-Arabia");

console.log(pair_1);
console.log(pair_2);
console.log(pair_3);
